                    <p>4 large eggs</p>
                    <p>1/2 cup all-purpose flour (spooned and leveled)</p>
                    <p>Pinch of salt</p>
                    <p>11 ounces semisweet chocolate, melted (2 1/2 cups chopped)</p>
                    <p>18 raspberries (36 if they are small)</p>
                    <p>Confectioners' sugar, for serving</p>
